/* --- Переключатель темы (тёмная/светлая/авто) --- */
(function initTheme() {
  const root = document.documentElement;
  const saved = localStorage.getItem("nivaTheme");
  if (saved) root.dataset.theme = saved;
  function syncBtn() {
    const btn = document.getElementById("themeToggle");
    if (!btn) return;
    const isLight = saved
      ? saved === "light"
      : window.matchMedia("(prefers-color-scheme: light)").matches;
    btn.textContent = isLight ? "☀️" : "🌙";
  }
  document.addEventListener("DOMContentLoaded", syncBtn);
  setTimeout(function() {
    const btn = document.getElementById("themeToggle");
    if (!btn) return;
    btn.addEventListener("click", function() {
      const cur = root.dataset.theme;
      const isLightSys = window.matchMedia("(prefers-color-scheme: light)").matches;
      const effective = cur || (isLightSys ? "light" : "dark");
      if (effective === "dark") {
        root.dataset.theme = "light";
        localStorage.setItem("nivaTheme", "light");
      } else {
        root.dataset.theme = "dark";
        localStorage.setItem("nivaTheme", "dark");
      }
      syncBtn();
    });
    syncBtn();
  }, 0);
})();

